import { Box, Chip, CircularProgress, Typography } from '@mui/material';
import { useQuery } from '@tanstack/react-query';

import { TestResultSummary } from '@/common/components/test_result_summary';
import { useResultDbClient } from '@/common/hooks/prpc_clients';
import {
  displayCompactDuration,
  parseProtoDuration,
} from '@/common/tools/time_utils';
import { GetArtifactRequest } from '@/proto/go.chromium.org/luci/resultdb/proto/v1/resultdb.pb';
import { TestResult } from '@/proto/go.chromium.org/luci/resultdb/proto/v1/test_result.pb';
import { TextDiffArtifactView } from '@/test_investigation/components/common/artifacts/content/text_diff';
import { useIsLegacyInvocation } from '@/test_investigation/context';
import { useFetchArtifactContentQuery } from '@/test_investigation/hooks/queries';
import { FailureReasonEntry } from '@/test_verdict/components/test_result_entry/failure_reason_entry';

import { CollapsibleArtifactSummarySection } from './collapsible_artifact_summary_section';
import { LegacyInvocationLinks } from './legacy_invocation_links';
import { PropertiesSection } from './properties_section';

interface ArtifactSummaryViewProps {
  currentResult: TestResult;
  selectedAttemptIndex: number;
}

export function ArtifactSummaryView({
  currentResult,
  selectedAttemptIndex,
}: ArtifactSummaryViewProps) {
  const resultDbClient = useResultDbClient();
  const { data: textDiffArtifact } = useQuery({
    ...resultDbClient.GetArtifact.query(
      GetArtifactRequest.fromPartial({
        name: `${currentResult.name}/artifacts/text_diff`,
      }),
    ),
    enabled: !!currentResult.name,
    staleTime: Infinity,
    retry: false,
  });

  const { data: textDiffContentData, isLoading: isLoadingTextDiffContent } =
    useFetchArtifactContentQuery({
      artifact: textDiffArtifact,
      enabled: !!textDiffArtifact,
    });

  const isLegacyInvocation = useIsLegacyInvocation();

  const [compactDuration] =
    (currentResult.duration &&
      displayCompactDuration(parseProtoDuration(currentResult.duration))) ||
    [];

  return (
    <>
      <Box
        sx={{
          pl: 1,
          pr: 1,
          pb: 1,
          display: 'flex',
          alignItems: 'center',
          gap: 1,
        }}
      >
        {compactDuration && (
          <Chip label={compactDuration} color="primary"></Chip>
        )}
        {isLegacyInvocation && (
          <LegacyInvocationLinks
            currentResult={currentResult}
            selectedAttemptIndex={selectedAttemptIndex}
          />
        )}
      </Box>
      {currentResult.failureReason && (
        <CollapsibleArtifactSummarySection
          title="Failure Reason"
          helpText="The error message(s) for this test failure, including stack traces."
        >
          <Box sx={{ pl: 1, pr: 1, pb: 1 }}>
            <FailureReasonEntry
              failureReason={currentResult.failureReason}
              inline={true}
            />
          </Box>
        </CollapsibleArtifactSummarySection>
      )}
      <CollapsibleArtifactSummarySection
        title="Summary"
        helpText="A HTML explanation of the test result generated by the test uploading code."
      >
        <Box sx={{ pl: 1, pr: 1, pb: 1 }}>
          {currentResult.summaryHtml ? (
            <TestResultSummary testResult={currentResult} />
          ) : (
            <Typography variant="body2" sx={{ fontStyle: ' italic' }}>
              No summary available.
            </Typography>
          )}
        </Box>
      </CollapsibleArtifactSummarySection>
      {textDiffArtifact && (
        <CollapsibleArtifactSummarySection
          title="Text Diff"
          helpText="A diff of the expected and actual text output of the test. Most often used with browser WPT tests."
        >
          <Box sx={{ pl: 1, pr: 1, pb: 1 }}>
            {isLoadingTextDiffContent ? (
              <Box
                sx={{
                  display: 'flex',
                  alignItems: 'center',
                  justifyContent: 'center',
                  p: 1,
                }}
              >
                <CircularProgress size={24} />
                <Typography sx={{ ml: 1 }}>Loading Text Diff...</Typography>
              </Box>
            ) : (
              <TextDiffArtifactView content={textDiffContentData?.data || ''} />
            )}
          </Box>
        </CollapsibleArtifactSummarySection>
      )}

      <PropertiesSection currentResult={currentResult} />
    </>
  );
}
