package lucicfg

import (
	"crypto/sha256"
	"fmt"
	"strconv"
	"strings"

	"go.starlark.net/starlark"
)

// namegen holds the state for the node names generator.
type namegen struct {
	// perFormat is mapping format string -> hash of inputs -> its unique index.
	perFormat map[string]map[[sha256.Size]byte]int
}

// generate produces a name from 'format' and 'inputs' obeying following rules:
//
// 1. 'format' is a format string with single '%s' in it somewhere. '%s' will be
//    substituted with some generated substring that contains only digits and
//    lowercase letters.
// 2. Within single lucicfg run, giving same 'format' and 'inputs' is guaranteed
//    to produce same output.
// 3. Within single lucicfg run, changing either 'format' or 'inputs' is
//    guaranteed to change the output.
// 4. There are no guarantees whatsoever for names generated by different
//    lucicfg runs, even if 'generate' takes same 'format' and 'inputs'. They
//    are usually repeatable, but this should not be relied upon.
func (n *namegen) generate(format string, inputs []string) (string, error) {
	if strings.Count(format, "%s") != 1 {
		return "", fmt.Errorf("expecting a format string with single '%%s', got %q", format)
	}

	if n.perFormat == nil {
		n.perFormat = make(map[string]map[[sha256.Size]byte]int, 1)
	}
	if n.perFormat[format] == nil {
		n.perFormat[format] = make(map[[sha256.Size]byte]int, 1)
	}

	// Calculate SHA256 of inputs.
	h := sha256.New()
	for _, s := range inputs {
		fmt.Fprintf(h, "%d\n%s\n", len(s), s)
	}
	var hash [sha256.Size]byte
	h.Sum(hash[:0])

	// Grab an index assigned to the hash if we've seen it before. Otherwise
	// allocate a new index (start with '1').
	perHash := n.perFormat[format]
	idx, ok := perHash[hash]
	if !ok {
		idx = len(perHash) + 1
		perHash[hash] = idx
	}

	return fmt.Sprintf(format, strconv.Itoa(idx)), nil
}

func init() {
	// generate_name(fmt, input) generates a unique string based on given inputs.
	declNative("generate_name", func(call nativeCall) (starlark.Value, error) {
		var format starlark.String
		var input starlark.Iterable
		if err := call.unpack(2, &format, &input); err != nil {
			return nil, err
		}

		// Convert 'input' to a list of strings.
		var inputStr []string
		iter := input.Iterate()
		defer iter.Done()
		var x starlark.Value
		for iter.Next(&x) {
			inputStr = append(inputStr, x.String())
		}

		name, err := call.State.names.generate(format.GoString(), inputStr)
		return starlark.String(name), err
	})
}
