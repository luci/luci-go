package tryjob

import "time"

// ID is an unique internal ID for a Tryjob in CV.
type ID int64

// ExternalID is the ID for this Tryjob in the external system (e.g.
// Buildbucket).
type ExternalID string

// Tryjob entity contains the information fetched from external system for
// a CV Tryjob.
type Tryjob struct {
	_kind string `gae:"$kind,Tryjob"`

	// ID is auto-generated by datastore.
	ID ID `gae:"$id"`
	// ExternalID is the ID of this Tryjob in the external system.
	//
	// Indexed to allow reusing the same Tryjob on different but semantically the
	// same Runs.
	ExternalID ExternalID
	// Status describes the status of this Tryjob.
	Status Status `gae:",noindex"`
	// EVersion is the entity version.
	//
	// It increments by one upon every successful modification.
	EVersion int `gae:",noindex"`
	// UpdateTime is the timestamp when this entity was last updated.
	UpdateTime time.Time `gae:",noindex"`
	// ActiveRuns are all Runs to be notified when this Tryjob has a status
	// update.
	ActiveRuns []string
	// Detail contains the detail information about this Tryjob.
	Detail *Detail
}
