func getErrLikeMsg(expected []interface{}) (string, string) {
	switch len(expected) {
	case 0:
		return "", ""
	case 1:
		s, ok := expected[0].(string)
		if !ok {
			return "", fmt.Sprintf("expected message must be string, not %T", expected[0])
		}
		return s, ""

	default:
		return "", "expected must have at most one argument"
	}
}
