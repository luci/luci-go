package main

import (
	"bufio"
	"fmt"
	"os"
	"path/filepath"
	"regexp"
	"sort"
	"strings"

	"go.chromium.org/luci/common/errors"
)

const DOC_DIR = "doc"
const DOC_FILE = "bigquery_tables.md"

func openDocFile(dir string) (*os.File, error) {
	dir, err := filepath.Abs(dir)
	if err != nil {
		return nil, err
	}
	filename := filepath.Join(dir, DOC_DIR, DOC_FILE)
	file, err := os.OpenFile(filename, os.O_RDWR|os.O_CREATE, 0640)
	if err != nil {
		if file != nil {
			file.Close()
		}
		return nil, err
	}
	return file, nil
}

func parseDoc(file *os.File) map[string]string {
	m := make(map[string]string)
	re := regexp.MustCompile("\\[(.*)\\]\\((.*)\\): (.*)")
	s := bufio.NewScanner(file)
	for s.Scan() {
		match := re.FindStringSubmatch(s.Text())
		if len(match) == 4 {
			tableKey := match[1]
			tableDesc := match[3]
			m[tableKey] = tableDesc
		}
	}
	return m
}

func truncateFile(file *os.File) error {
	err := file.Truncate(0)
	if err != nil {
		return err
	}
	_, err = file.Seek(0, 0)
	if err != nil {
		return err
	}
	return nil
}

func getLines(tableDescriptions map[string]string) []string {
	keys := []string{}
	for k := range tableDescriptions {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	lines := []string{
		"This file is is generated by bqschemaeupdater. Please do not modify it directly.",
		"",
		"# BigQuery Tables",
		"",
	}
	lineTemplate := "[%s](https://bigquery.cloud.google.com/table/%s): %s"
	for _, k := range keys {
		lines = append(lines, fmt.Sprintf(lineTemplate, k, k, tableDescriptions[k]))
	}
	return lines
}

func updateTableList(td tableDef, dir string) error {
	file, err := openDocFile(dir)
	defer file.Close()
	if err != nil {
		return errors.Annotate(err, "cound not open doc file").Err()
	}
	tableDescriptions := parseDoc(file)
	keyTemplate := "%s:%s.%s"
	k := fmt.Sprintf(keyTemplate, td.ProjectID, td.DataSetID, td.TableID)
	tableDescriptions[k] = strings.Replace(td.Description, "\n", " ", -1)
	err = truncateFile(file)
	if err != nil {
		return errors.Annotate(err, "cound not truncate doc file").Err()
	}
	lines := getLines(tableDescriptions)
	file.WriteString(strings.Join(lines, "\n"))
	file.Sync()
	return nil
}
